# Задача 10: 
# На столе лежат n монеток. 
# Некоторые из них лежат вверх решкой, а некоторые – гербом. Определите минимальное число монеток, которые нужно перевернуть, 
# чтобы все монетки были повернуты вверх одной и той же стороной. Выведите минимальное количество монет, которые нужно перевернуть

def min_flips(coins):
    heads = sum(coins)  # Количество монет, лежащих вверх решкой
    tails = len(coins) - heads  # Количество монет, лежащих вверх гербом
    return min(heads, tails)

# Пример использования
coins = [1, 0, 1, 1, 0]  # Пример расположения монет, 1 - решка, 0 - герб
min_flips_needed = min_flips(coins)
print("Минимальное количество монет, которые нужно перевернуть:", min_flips_needed)

